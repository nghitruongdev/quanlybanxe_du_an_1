    public void fillComboLoaiHang() {
        HashSet<String> set = new HashSet();
        new LoaiHangDAO().selectAll().forEach((lh) -> {
            set.add(lh.getIdLH());
        });
        cboDSP_IdLH.setModel(new DefaultComboBoxModel(set.toArray()));
    }
    public void fillComboNSX() {
        HashSet<String> set = new HashSet();
        new NhaSanXuatDAO().selectAll().forEach((nv) -> {
            set.add(nv.getIdNSX());
        });
        cboDSP_IdNSX.setModel(new DefaultComboBoxModel(set.toArray()));
    }
    
    
    public DongSanPham getForm_DSP(){
        DongSanPham dsp = new DongSanPham();
        dsp.setIdDongSP(txtID_DongSP.getText());
        dsp.setTenDongSP(txtTenDongSP.getText());
        dsp.setIdLH((String) cboDSP_IdLH.getSelectedItem());
        dsp.setIdNSX((String) cboDSP_IdNSX.getSelectedItem());
        return dsp;
    }
    
    public void clearForm_DSP(){
        
            txtID_DongSP.setText("");
            txtTenDongSP.setText("");
            if (cboDSP_IdLH.getItemCount() > 0) {
                cboDSP_IdLH.setSelectedIndex(0);
            }
            if (cboDSP_IdNSX.getItemCount() > 0) {
                cboDSP_IdNSX.setSelectedIndex(0);
            }
        
        
    }
    void setFrom_DSP(){
        if (dsp== null) {
            clearForm_DSP();
            return;
        }
        txtID_DongSP.setText(dsp.getIdDongSP());
        txtTenDongSP.setText(dsp.getTenDongSP());
        cboDSP_IdLH.setSelectedItem(dsp.getIdLH());
        cboDSP_IdNSX.setSelectedItem(dsp.getIdNSX());
    }

    public void setForm_DSP(DongSanPham dsp) {
        this.dsp = dsp;
        setFrom_DSP();
    }
    
