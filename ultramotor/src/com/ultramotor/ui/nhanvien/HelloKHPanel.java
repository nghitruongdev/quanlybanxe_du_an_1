    public static class MovingText extends JPanel {
        int x = 0;
        int y = 100;
        public void paint(Graphics g){
            super.paint(g);
            Graphics2D g2 = (Graphics2D)g;
            Font font = new Font("Tahoma",Font.BOLD+Font.PLAIN,100);
            g2.setFont(font);
            g2.setColor(Color.red);
            g2.drawString("Xin chao khach hang", x, y);
            
            try {
                Thread.sleep(100);
            } catch (Exception ex) {
            }
            x+=10;
            if (x > this.getWidth()) {
                x = 0;
            }
            repaint();
        }
    }
