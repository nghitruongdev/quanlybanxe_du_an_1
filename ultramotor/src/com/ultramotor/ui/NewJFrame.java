    void fillTableLoaiHang() {
        DefaultTableModel model = (DefaultTableModel) tblLoaiHang.getModel();
        model.setRowCount(0);
        try {
            List<LoaiHang> list = dao_LH.selectAll();
            for (LoaiHang lh : list) {
                Object[] row = {
                    lh.getIdLH(),
                    lh.getTenLoaiHang()
                };
                model.addRow(row);
            }
        } catch (Exception e) {
            e.printStackTrace();
            MsgBox.inform("Lỗi truy vấn dữ liệu!");
        }
    }

